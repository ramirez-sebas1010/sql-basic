/*
	ORDER BY -> Nos permite a nosotros ordenar por cierta columna
	OBS:
	SIEMPRE ULTIMO CLAUSULA
*/


-- ORDENAR POR COLUMNA
SELECT columna
FROM TABLA
ORDER BY columna1,columna2,...columna3


-- ORDENAR POR NUMERO COLUMNA
SELECT columna
FROM TABLA
ORDER BY 1,2,3

